def qmc1_decrypt(data: bytearray) -> None:
    for i, _value in enumerate(data):
        data[i] ^= PRIVKEY[(i % 0x7FFF) & 0x7F] if i > 0x7FFF else PRIVKEY[i & 0x7F]
